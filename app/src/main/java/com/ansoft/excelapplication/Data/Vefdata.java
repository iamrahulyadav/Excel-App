public class Vefdata {

    String voyageNumber;
    String loadPort;
    String cargo;
    String bargeVol;
    String shoreVol;
    String vefFactor;
    String pass;

    public String getVoyageNumber() {
        return voyageNumber;
    }

    public void setVoyageNumber(String voyageNumber) {
        this.voyageNumber = voyageNumber;
    }

    public String getLoadPort() {
        return loadPort;
    }

    public void setLoadPort(String loadPort) {
        this.loadPort = loadPort;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getBargeVol() {
        return bargeVol;
    }

    public void setBargeVol(String bargeVol) {
        this.bargeVol = bargeVol;
    }

    public String getShoreVol() {
        return shoreVol;
    }

    public void setShoreVol(String shoreVol) {
        this.shoreVol = shoreVol;
    }

    public String getVefFactor() {
        return vefFactor;
    }

    public void setVefFactor(String vefFactor) {
        this.vefFactor = vefFactor;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
